let horver_laptop = document.getElementById("hover_laptop");
let hover_Laptop = document.getElementById("hover_laptop_id");
horver_laptop.addEventListener('mousemove', function(){
    hover_Laptop.style.display = "block";
});

horver_laptop.addEventListener('mouseout', function(){
    hover_Laptop.style.display = "none";
});

hover_Laptop.addEventListener('mousemove', function(){
    hover_Laptop.style.display = "block";
});

hover_Laptop.addEventListener('mouseout', function(){
    hover_Laptop.style.display = "none";
});