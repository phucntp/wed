let hover_repair = document.getElementById("hover_repair");
let hover_Repair = document.getElementById("hover_repair_id");
hover_repair.addEventListener('mousemove', function(){
    hover_Repair.style.display = "block";
});

hover_repair.addEventListener('mouseout', function(){
    hover_Repair.style.display = "none";
});

hover_Repair.addEventListener('mousemove', function(){
    hover_Repair.style.display = "block";
});

hover_Repair.addEventListener('mouseout', function(){
    hover_Repair.style.display = "none";
});